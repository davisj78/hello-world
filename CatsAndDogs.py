"""
raining_cats_and_dogs.py
------------------------
Displays the sentence:  It is raining cats and dogs.
Demonstrates:
• variables that hold strings
• string concatenation with the + operator
• basic documentation (module docstring + inline comments)
"""

# ---------- variable definitions ----------
c = "cats"          # the first animal
d = "dogs"          # the second animal

# Assemble the final sentence (note the spaces where needed)
s = "It is raining " + c + " and " + d + "."

# ---------- output ----------
print(s)             # prints: It is raining cats and dogs.

